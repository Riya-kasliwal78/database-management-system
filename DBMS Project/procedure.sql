CREATE SEQUENCE customer_seq
    START WITH 1
    INCREMENT BY 1;

CREATE OR REPLACE TRIGGER customer_id_trigger
BEFORE INSERT ON CUSTOMERS
FOR EACH ROW
WHEN (NEW.customer_id IS NULL)
BEGIN
    SELECT customer_seq.NEXTVAL INTO :NEW.customer_id FROM DUAL;
END;
/


