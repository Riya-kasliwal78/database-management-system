CREATE SEQUENCE service_id_seq
    START WITH 1
    INCREMENT BY 1;

CREATE OR REPLACE TRIGGER trg_service_id
BEFORE INSERT ON Services
FOR EACH ROW
BEGIN
    IF :NEW.service_id IS NULL THEN
        SELECT service_id_seq.NEXTVAL INTO :NEW.service_id FROM DUAL;
    END IF;
END;
/


